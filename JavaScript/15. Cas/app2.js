// String mozemo zapisati na nekoliko nacina:

// 1. Koristeci duple navodnike:
"Danas je bilo promeljivo vreme"

// 2. Koristeci duple navodnike
'Danas je bilo promenljivo vreme.'

// 3. Template literals (Back-tics sintaksa)
`Danas je bilo promenljivo vreme.`

// Dupli Navodnici:
"Danas je bilo 'promenljivo' vreme."

"Danas je bilo \"promenljivo\" vreme." // koristimo Back-Slash kako bi ubacili duple u duple navodnike


// Obicni Navodnici:
'Danas je bilo "promenljivo" vreme.'

'Danas je bilo \'promenljivo\' vreme.'


